package Lab;

public class Shape {
	protected String adi;
	protected Double alan;
	protected Shape(String adi, Double alan) {
		super();
		this.adi = adi;
		this.alan = alan;
	}
	protected String getAdi() {
		return adi;
	}
	protected void setAdi(String adi) {
		this.adi = adi;
	}
	protected Double getAlan() {
		return alan;
	}
	protected void setAlan(Double alan) {
		this.alan = alan;
	}
	

	
		
	}
	

	


